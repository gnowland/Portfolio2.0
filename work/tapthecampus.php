    <h2>Project Brief</h2>
      <p>The idea for Tap The Campus originated as a Sophomore attending the University of Delaware. Frank Trofa and I identified that there was an untapped opportunity for an online directory of local venues where they could advertise nightly specials and connect with patrons. At this time Twitter, Foursquare, and even Facebook were quickly becoming popular on campus, and restaurant owners had not discovered how to effectively promoting their businesses online.</p>
    <h2>Project Strategy</h2>
      <p>Frank and I began developing the details of an app that would allow patrons to evaluate a "heat index" of the bars in their area all in one place. Users would be able to check-in to a place and tell their friends what they felt about the current ambiance using a "mood scale". They could choose to receive notifications about drink specials, event information, and other relevant daily updates. Restaurant owners could manage their directory page, effortlessly allowing them to stay connected with their most valued patrons.</p>
      <p>After pitching a start-up incubator at the Early Stage East conference in Philadelphia in the Summer of 2012 Frank and I were put into contact with Rich, the owner of LoSo. LoSo also provided event notifications and rewards for visiting local venues and had already established itself in the greater Philadelphia market. As the location-based app space was consolidating, and facing fierce competition from services such as Yelp and Foursquare, Frank and I decided to partner with Rich and LoSo.</p>
    <div id="imac">
