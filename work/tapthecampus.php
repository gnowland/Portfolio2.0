    <h2>Project Result</h2>
     <p>We commenced our endeavor for a complete re-brand, redevelopment, and relaunch of Tap the Campus under the LoSo name. We were determined to offer an app that would compete with Yelp and Foursquare, but provide a more focused feature-set that would engage working professionals. We were completed the combination of our original Tap the Campus concept onto the existing LoSo arctetcture, but unfortunately further work on the project has been suspended while negotiations with former investors over original LoSo assets and intellectual property is resolved.</p>
    <p>For the 12-month period the LoSo app was active it had gained local recognition and over 8,000 users in the Philadelphia metro area alone with plans on expanding to other cities within the following year.</p>
