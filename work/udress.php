    <h2>Project Brief</h2>
      <p>UDress Magazine brought me on me as their Web Chairman after my website for <a href="work.php?project=delride">DelRide SnowCorp</a> gained attention in the University community. UDress Magazine was interested in building a similar online community for their members, where participants could discuss fashion together and share their favorite trends. They hoped to not only showcase the physical magazine, but also compliment it with online-exclusive editorial content. I worked closely with the UDress executive team to understand what features they were looking for in an online community website and how they wanted to manage the relationship between the physical magazine and the online entity. Especially important to them was the ability to keep readers interested in the magazine between bi-yearly issues by providing a constant supply of new content. I would recruit a web team to help write content and perform administrative maintenance.</p>
    <h2>Project Strategy</h2>
      <p>Due to my previous experience creating a community site for DelRide I had an understanding of what would work for UDress. However, since this was going to be collaborative website, as opposed to running the entire site myself, I had to take into account ease of use in the backed. At this time Drupal (the CMS I used for DelRide) was extremely powerful, but not very easy to learn how to use. I decided to develop UDressâ€™s website on the Joomla framework instead. I led the web team in the installation and customization of the Joomla configuration, modifying PHP templates, nodes, and modules. I developed a workflow to digitize the magazine and present it in an interactive flash interface for online reading. I performed search engine optimization diagnostics and taught content editors how to format their posts to boost search rankings.</p>
