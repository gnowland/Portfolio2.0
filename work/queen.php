    <h2>Project Result</h2>
      <p>We developed a website that put large, beautiful images front and center to convey the concealed elegance of the building. The homepage featured a rotating set of images as its background. We incorporated an interactive timeline, picture gallery, and short film about the history of the theater told through the memories of Wilmington residents who had been patrons of the theater in their youth.</p>
      <p>The website was an important resource advocating reinvestment in the theater. Philadelphia public radio station WXPN announced that they would open a second studio and host a branch of their syndicated <i>World Caf√© Live</i> show at the Queen. Wilmington real estate developers Buccini/Pollin Group and city officials soon joined in the initiative to restore the Queen Theater and in October 2009, six months after the launch of the website, the $25 million required for restoration was secured and the Light Up The Queen Foundation announced a Spring 2011 opening. Today many local and touring acts have shows at the theater and it remains a driving success in the downtown Wilmington revitalization movement.</p>
