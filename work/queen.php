    <h2>Project Brief</h2>
      <p>I was chosen as lead web developer and project manager for the Light Up The Queen, a class project my junior year of college. The Queen Theater, a 45,000 sq ft theater in downtown Wilmington, Delaware had been abandoned and left to decay for five decades until the Light Up The Queen Foundation stepped in to revive it. The foundation wanted a website that would highlight the theater's rich past and historical significance to inspire community and investor interest in the redevelopment project.</p>
    <h2>Project Strategy</h2>
      <p>One of the most striking features of the Queen Theater was the state in which it had been left. Ancient movie projectors decayed with their last film reels still in place. The decorative wallpaper, once a glimmering red and gold, was peeling from the walls. Rays of light peaked through the ceiling, simultaneously illuminating the structure's ornate details while exposing its disintegration. We yearned to capture the potential concealed under years of rust and deterioration and hoped that our website would effectively convey that feeling to others.</p>
